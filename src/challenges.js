function footballPoints(wins, ties) {
  return wins * 3 + ties;
}

// Desafio 6
function highestCount(numArray) {
  return [...numArray]
    .sort((a, b) => b - a)
    .filter((n, idx, array) => n == array[0])
    .reduce((t) => ++t, 0);
}

